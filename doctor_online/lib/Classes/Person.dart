import 'dart:developer';

class Person {
  var firstName,lastName, phoneNo, email, PType, gender, city, country, state;

  Person(var fName,var lName, var PhoneNo, var Email, var pType, var Gender, var City,
      var Country, var State) {
        firstName=fName;
        lastName=lName;
        phoneNo=PhoneNo;
        email=Email;
        PType=pType;
        gender=Gender;
        city=City;
        country=Country;
        state=State;
      }
}
